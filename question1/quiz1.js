//!  Answer:
// setTimeout executes the code only once after the specified delay, while setInterval executes the code repeatedly at the specified interval.
// setTimeout : کد داده شده را بعد از تایمی که داده شده و تنها یکبار اجرا میکند
// اما setInterval کد داده شده را مکرر در فواصل زمانی مشخص (براساس تایمی که بهش داده شده)اجرا میکند